    private void idEliminarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idEliminarKeyTyped
        char validar = evt.getKeyChar();

        if(Character.isLetter(validar) || Character.isWhitespace(validar)){
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_idEliminarKeyTyped

    private void bottonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bottonEliminarActionPerformed

        String idText = this.idEliminar.getText();
        
       
        
        if(!idText.isEmpty()){
            
            int id = Integer.parseInt(idText);
            
            if(registro.eliminarProducto(id)){
                
                JOptionPane.showMessageDialog(this, "Producto eliminado correctamente", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
            }else{
                
                JOptionPane.showMessageDialog(this, "El producto con ID: "+id+" no existe, verifica en la lista de productos", "Error", JOptionPane.ERROR_MESSAGE);
                listar.setVisible(true); 
                
            }
        }else{
            JOptionPane.showMessageDialog(this, "Tienes que rellenar la casilla", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_bottonEliminarActionPerformed

